// USE
use crate::*;

//////////////////////////////////=////////////////////////////////=////////////////////////////////
// PLUGIN
pub struct AppStatePlugin;
impl Plugin for AppStatePlugin {
    fn build(
        &self,
        app: &mut App,
    ) {
        app.add_state::<AppState>();
    }
}

//////////////////////////////////=////////////////////////////////=////////////////////////////////
// ENUM
#[derive(Default, States, Debug, Clone, Copy, PartialEq, Eq, Hash)]
pub enum AppState {
    #[default]
    Loading,
    MainMenu,
    Gameplay,
    Editor,
}